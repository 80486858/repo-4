#!/bin/bash
set -ex

psql -v ON_ERROR_STOP=1 --username "$POSTGRES_USER" --dbname "$POSTGRES_DB" <<-EOSQL
	CREATE USER "oc_$POSTGRES_USER" WITH PASSWORD '$POSTGRES_PASSWORD';
	GRANT ALL PRIVILEGES ON DATABASE "$POSTGRES_DB" TO "oc_$POSTGRES_USER";
	/*for backwarts compatibility*/
	CREATE USER oc_admin WITH PASSWORD '$POSTGRES_PASSWORD';
	GRANT ALL PRIVILEGES ON DATABASE "$POSTGRES_DB" TO oc_admin;
EOSQL

set +ex
